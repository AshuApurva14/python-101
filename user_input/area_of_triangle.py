# This script calculates  Area of Triangle 

# Let's first define two variables which stores value of Base and Height.

# Base = Base of the Triangle
# Height = Heiight of the Triangle

base=input("Base of the Triangle ")
height=input("Height of the Triangle ")

area_of_triangle=1/2 * float(base) * float(height)

print(f'Area of Triangle is {area_of_triangle}')