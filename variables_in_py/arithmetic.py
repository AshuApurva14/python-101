# Write a program which defines two variables and prints the result of addition, subtraction, multiplication, and division of the two variables.

var1 = 10
var2 = 20

# Addition

add = var1+ + var2
print("Addition of two Variables:", add)

subtract = var1 - var2
print("Substraction of two variables:", subtract)

multiply = var1 * var2
print("Multiplication of two variables:", multiply)

division = var1 / var2 
print("Division of two variables:", division)

# Output
# Addition of two Variables: 30 
# Substraction of two variables: -10
# Multiplication of two variables: 200
# Division of two variables: 0.5
