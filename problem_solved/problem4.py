'''
 Create a program that asks a user for two numbers, x and y.
  If the x is greater than y, print back to the user "x>y". 
  If x is less than y, print back to the user "x<y".
   If x is equal to y, print back to the user "x=y".

'''

x=int(input("Enter first number "))
y=int(input("Enter second number "))

if x>y:
    print("x>y")

elif x<y:
    print("x<y")

else:
    print("x=y")

